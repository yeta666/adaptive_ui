package com.adaptive.ui.type;

/**
 * 所有用户类型
 * Created by yeta on 2017/4/6/006.
 */
public interface UserType {
    public static final String TYPE1 = "活跃型";
    public static final String TYPE2 = "沉思型";
    public static final String TYPE3 = "感悟型";
    public static final String TYPE4 = "直觉型";

    /**
     * 每种类型的解释
     *
     * 1.活跃型与沉思型
     * 活跃型：
     *      ① 倾向于通过积极地做一些事、讨论、应用或解释给别人听来掌握信息
     *      ② 喜欢集体工作
     * 沉思型：
     *      ① 更喜欢安静地思考问题
     *      ② 倾向于独立工作。
     *
     * 2.感悟型与直觉型
     * 感悟型：
     *      ① 喜欢学习事实
     *      ② 不喜欢复杂情况和突发情况
     *      ③ 痛恨测试一些在课堂里没有明确讲解过的内容
     *      ④ 对细节很有耐心
     *      ⑤ 擅长记忆事实和做一些现成的工作
     *      ⑥ 更实际和仔细
     *      ⑦ 不喜欢与现实生活没有明显联系的课程
     * 直觉型：
     *      ① 倾向于发现某种可能性和事物间的关系
     *      ② 喜欢革新不喜欢重复
     *      ③ 更擅长于掌握新概念
     *      ④ 更能理解抽象的数学公式
     *      ⑤ 工作得更快更具有创新性
     *      ⑥ 不喜欢那些包括许多需要记忆和进行常规计算的课程
     *
     */
}
